class Solution:
    def increasingTriplet(self, nums: List[int]) -> bool:
        def helper(start: int, monotonic_stack: List[int]) -> bool:
            i = start
            while i < len(nums):
                if monotonic_stack and monotonic_stack[-1] == nums[i]:
                    i += 1
                    continue
                elif monotonic_stack and monotonic_stack[-1] > nums[i]:
                    j = i
                    while j < len(nums) and monotonic_stack[-1] >= nums[j]:
                        j += 1
                    if helper(j, monotonic_stack[:]):
                        return True

                    # iterate until the end of the trough
                    while i + 1 < len(nums) and nums[i + 1] <= nums[i]:
                        i += 1

                value = nums[i]
                while monotonic_stack and monotonic_stack[-1] >= value:
                    monotonic_stack.pop()
                monotonic_stack.append(value)
                i += 1
                if len(monotonic_stack) >= 3:
                    return True
            return False

        return helper(0, [])


